    public void ListarTabelas() {
        DefaultTableModel table = (DefaultTableModel) tblEstoque.getModel();
        CarregaTabelas tbl = new CarregaTabelas();
        table.setNumRows(0);
        for (Model_Estoque model : tbl.CarregaTabelaN()) {

            table.addRow(new Object[]{
                model.getId_estoque(),
                model.getDescricao(),
                model.getQuant(),
                model.getData_cadastro(),
                model.getUser_cad(),
                model.getStatus(),
                model.getData_cadastro()
            });
        }
    }

