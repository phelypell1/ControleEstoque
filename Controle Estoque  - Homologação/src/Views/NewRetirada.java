    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        RetEstoqueM m = new RetEstoqueM();
        m.setData_alteracao(Date.valueOf(LocalDate.now()));

        Connection con = ConexaoMySql.getConnection();
        PreparedStatement pst;
        String sql;
        try {
            sql = "insert into HtsEstoque(estoque_id, item_retirado, data_alteracao, editado, quant_retirado,obs) "
                    + "values('" + txtId.getText() + "','"+txtDescricao.getText()+"','" + m.getData_alteracao() + "','" + txtCadPor.getText() + "','" + txtQuantR.getText() + "','"+txtObservacao.getText()+"')";
            pst = con.prepareStatement(sql);
            pst.execute();
            valor = Integer.valueOf(txtQuant.getText());
            valorR = Integer.valueOf(txtQuantR.getText());
            result = valor - valorR;
           
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "OK !" + e.getMessage());
            ConexaoMySql.CloseConection(con);
        }catch(Error d){
            txtId.getText().equals("");
            JOptionPane.showMessageDialog(null, d.getMessage());
            ConexaoMySql.CloseConection(con);
        }
        String sql2;
        
        try {
            sql2 = "update Estoque set quant ='"+result+"' where id_estoque = '"+txtId.getText()+"'";
            pst = con.prepareStatement(sql2);
            pst.execute();
            ListarTabelas();
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Atenção, erro ao Atualizar");
            ConexaoMySql.CloseConection(con);
        }catch(Error d){
            boolean equals = txtId.getText().equals("");
            JOptionPane.showMessageDialog(null, d.getMessage());
            ConexaoMySql.CloseConection(con);
        }
        
        txtId.setText("");
        txtDescricao.setText("");
        txtQuant.setText("");
        txtData.setText("");
        txtCadPor.setText("");
        txtStatus.setText("");
        txtQuantR.setText("");
        txtObservacao.setText("");
    }//GEN-LAST:event_jButton1ActionPerformed
